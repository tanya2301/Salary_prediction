import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression
from sklearn import linear_model
from sklearn import metrics

"""Reading Dataset"""

df=pd.read_csv("Salary_dataset.csv")
print(df)

"""Data visualization"""

plt.plot(df['Candidate'],df['Salary'])
plt.xlabel("Candidate")
plt.ylabel("Salary")

plt.scatter(df['Interview_score'],df['Salary'])
plt.xlabel("Interviewscore")
plt.ylabel("Salary")

plt.bar(df['Experience'],df['Salary'])
plt.xlabel("Experience")
plt.ylabel("Salary")

"""Apply Regression"""

reg=linear_model.LinearRegression()
reg.fit(df[['Candidate','Interview_score','Experience']],df['Salary'])

"""Calculating regression coefficient"""

print(reg.coef_)

"""Calculating regression interception"""

print(reg.intercept_)

"""Predicting the result"""

yp=reg.predict(df[['Candidate','Interview_score','Experience']])

plt.plot(df['Candidate'],yp,color='k')
plt.scatter(df['Candidate'],df['Salary'],color='r')

plt.plot(df['Interview_score'],yp,color='k')
plt.scatter(df['Interview_score'],df['Salary'],color='r')

plt.plot(df['Experience'],yp,color='k')
plt.scatter(df['Experience'],df['Salary'],color='r')

"""Evaluation by r2 score"""

metrics.r2_score(yp,df['Salary'])

metrics.accuracy_score(yp,df['Salary'])

"""Testing """

reg.predict([[1,7,1]])

reg.predict([[5,7,1]])

reg.predict([[7,3,2]])

reg.predict([[1,7,3]])